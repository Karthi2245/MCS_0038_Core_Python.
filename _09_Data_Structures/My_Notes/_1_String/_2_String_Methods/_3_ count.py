'''
Count():
Returns the number of times a specified value occurs in a string.

Syntax:
string.count()

'''
# Ex:
word = "Count is something that count countable strings which are coming repeatedly"
print("Actual string:", word)
print("After using count function:", word.count("count"))

# Ex:2: we can also count spaces too.
word = "Count is something that count countable strings which are coming repeatedly"
print("Actual string:", word)
print("After using count function:", word.count(" "))